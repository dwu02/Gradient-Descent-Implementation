def displayUnivariate(optimum,learning_rates,epoch_max,best_epoch,predictor_name,response_name,training_num,test_num,best_learning_rate,cur_thetas,cur_mse,min_thetas,min_mse):
    if optimum:
        print(f'Univariate Linear Regression - Optimum')
        print(f'Optimal Learning Rate between {learning_rates[0]} - {learning_rates[1]} for a max Epoch of {epoch_max}')
        print(f'Predictor: {predictor_name}')
        print(f'Response: {response_name}')
        print(f'Training Set # of Points: {training_num}')
        print(f'Test Set # of Points: {test_num}')
        print(f'Best Learning Rate = {best_learning_rate}')
        print(f'Best Epoch: {best_epoch}')
        print(f'Thetas = {min_thetas}')
        print(f'MSE =  {min_mse}')
    else:
        print(f'Univariate Linear Regression - Non-Optimum')
        print(f'Predictor: {predictor_name}')
        print(f'Response: {response_name}')
        print(f'Training Set # of Points: {training_num}')
        print(f'Test Set # of Points: {test_num}')
        print(f'Learning Rate = {learning_rates[0]}')
        print(f'Total Epochs = {epoch_max}')
        print(f'Thetas = {cur_thetas}')
        print(f'MSE = {cur_mse}')

